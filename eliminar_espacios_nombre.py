# Variable con nombre, con espacios en blanco al principio y al final
name = "\tAntonio  \n";

# Imprimo el contenido de la variable
print(name);

# Elimino los espacios de la izquierda
print(name.lstrip());

# Elimino los espacios de la derecha
print(name.rstrip());

# Elimino los espacios de ambos lados
print(name.strip());
